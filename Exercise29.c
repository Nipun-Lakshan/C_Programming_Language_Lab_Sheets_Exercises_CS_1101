// Variable in C (Supplementary Worksheet Week 01 - Question 24)

#include <stdio.h>

int main()
{
    // Variable Declarations
    float a = 11.2;
    float b = 2.2;

    // Calculations & Print Results
    printf("\n%.2f + %.2f = %.2f", a, b, (a + b));
    printf("\n%.2f - %.2f = %.2f", a, b, (a - b));
    printf("\n%.2f * %.2f = %.2f", a, b, (a * b));
    printf("\n%.2f / %.2f = %.2f\n", a, b, (a / b));

    return 0;
}