// Declaring Variables and Format Specifiers (Supplementary Worksheet Week 01 - Question 29)

#include <stdio.h>

int main()
{
    // Variable Declaration
    int a = 10;

    // Print Results
    printf("\na = 10 and a-- : %d", (a--)); // 10
    a = 10;
    printf("\na = 10 and --a : %d\n", (--a)); // 09

    return 0;
}
